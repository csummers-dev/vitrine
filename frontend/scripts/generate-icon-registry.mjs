/*
 * generate-icon-registry.mjs — emit src/utils/iconRegistry.generated.ts
 *
 * Why: Icon.vue used to `import * as LucideIcons from "@lucide/vue"`, which
 * pulled the ENTIRE icon set (~2,940 icons → a 727 kB / 195 kB-gz chunk loaded
 * and parsed on every cold start) just to look icons up by name at runtime.
 * This script scans the source for every kebab-case string literal that is a
 * REAL lucide icon name and emits an explicit `name → component` map, so the
 * bundle ships only the icons the app actually references (tree-shaken).
 *
 * Harvest is deliberately broad — ANY quoted kebab literal that matches a lucide
 * export is kept — so an icon name produced by a helper (colIcon(), iconName(),
 * file-type maps, command/menu `icon:` fields, ternaries) can't be missed. The
 * cost of a false positive is one extra ~0.3 kB icon; the cost of a false
 * negative is a blank icon, so we err toward inclusion.
 *
 * Re-run whenever icons change:  node scripts/generate-icon-registry.mjs
 * (Icon.vue dev-warns at runtime if it ever renders a name not in the map.)
 */
import { readFileSync, writeFileSync, readdirSync, statSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import * as Lucide from "@lucide/vue";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const srcDir = join(root, "src");
const outFile = join(srcDir, "utils", "iconRegistry.generated.ts");

const walk = (dir) => {
  const out = [];
  for (const e of readdirSync(dir)) {
    const p = join(dir, e);
    if (statSync(p).isDirectory()) out.push(...walk(p));
    else if (/\.(vue|ts)$/.test(e) && !e.endsWith(".d.ts")) out.push(p);
  }
  return out;
};

const pascal = (s) =>
  s
    .split(/[-_]/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join("");

// Collect every kebab-case quoted literal in the source tree.
const candidates = new Set();
for (const file of walk(srcDir)) {
  if (file === outFile) continue;
  const text = readFileSync(file, "utf8");
  for (const m of text.matchAll(
    /['"]([a-z][a-z0-9]*(?:-[a-z0-9]+)+|[a-z]+)['"]/g
  )) {
    const s = m[1];
    if (/^[a-z][a-z0-9-]*$/.test(s)) candidates.add(s);
  }
}

// Keep only the ones that are genuine lucide icons.
const kept = [...candidates]
  .filter((name) => typeof Lucide[pascal(name)] !== "undefined")
  .sort();

const pascalNames = kept.map(pascal);
const body = `// AUTO-GENERATED by scripts/generate-icon-registry.mjs — DO NOT EDIT BY HAND.
// Re-run \`node scripts/generate-icon-registry.mjs\` after adding/removing icons.
// Explicit import map so the bundle tree-shakes to only the ${kept.length} icons
// the app references, instead of the full ~2,940-icon lucide set.
import type { Component } from "vue";
import {
${pascalNames.map((p) => `  ${p},`).join("\n")}
} from "@lucide/vue";

export const iconRegistry: Record<string, Component> = {
${kept.map((name, i) => `  "${name}": ${pascalNames[i]},`).join("\n")}
};
`;

writeFileSync(outFile, body);
console.log(
  `iconRegistry.generated.ts written: ${kept.length} icons (from ${candidates.size} candidate literals).`
);
